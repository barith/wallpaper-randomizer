"""Setup script for wallpaper-randomizer."""

from setuptools import setup, find_packages

with open("README.md", "r", encoding="utf-8") as fh:
    long_description = fh.read()

with open("requirements.txt", "r", encoding="utf-8") as fh:
    requirements = [line.strip() for line in fh if line.strip()
                    and not line.startswith("#")]

setup(
    name="wallpaper-randomizer",
    version="1.0.0",
    author="Wallpaper Randomizer Contributors",
    description="Set random wallpapers from Reddit subreddits",
    long_description=long_description,
    long_description_content_type="text/markdown",
    url="https://github.com/yourusername/wallpaper-randomizer",
    packages=find_packages(),
    classifiers=[
        "Programming Language :: Python :: 3",
        "License :: OSI Approved :: MIT License",
        "Operating System :: OS Independent",
        "Environment :: Console",
    ],
    python_requires=">=3.7",
    install_requires=requirements,
    entry_points={
        "console_scripts": [
            "wallpaper-randomizer=wallpaper_randomizer.__main__:main",
        ],
    },
)
